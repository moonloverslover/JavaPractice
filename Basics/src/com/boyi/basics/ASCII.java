package com.boyi.basics;

public class ASCII {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
      System.out.println((int)'0');  //48
      System.out.println((int)'a');  //97
      System.out.println((int)'z');  //122
      System.out.println((int)'A');  //65
      System.out.println((int)'Z');  //90


	}

}
